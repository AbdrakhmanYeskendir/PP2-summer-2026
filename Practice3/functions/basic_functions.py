# BASIC FUNCTIONS
# A function is a reusable block of code.

# Example 1: Simple function
def greet():
    print("Hello, welcome to Practice 3!")


greet()


# Example 2: Function with one task
def show_course():
    print("Course: Programming Principles 2")


show_course()


# Example 3: Function that prints student information
def show_student_info():
    name = "Yeskendir"
    university = "KBTU"
    print("Student:", name)
    print("University:", university)


show_student_info()


# Example 4: Function used multiple times
def print_separator():
    print("-" * 30)


print_separator()
print("First section")
print_separator()
print("Second section")
print_separator()